//Square Root
console.log(Math.sqrt(9));

//Max and Min
console.log(Math.max(33, 44, 64, 86, 8, 0, 35, 5));
console.log(Math.min(33, 44, 64, 86, 8, 0, 35, 5));

console.log(Math.PI);

console.log(Math.random());

//trunc ira remover os numeros decimais
console.log(Math.trunc(Math.random() * 6) + 1);

//rounding integers
console.log(Math.trunc(23.3));
console.log(Math.round(23.4));

//round up
console.log(Math.ceil(23.5));

//round down
console.log(Math.floor(34.5));

//negative numbers
console.log(Math.floor(-23.5)); //it is better to use floor than trunc
console.log(Math.trunc(-23.5));

//rounding decimals
console.log((2.7).toFixed(0));
console.log((2.7).toFixed(3));
console.log(+(2.346).toFixed(2));
